    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if(t1.getText().equals(""))
            JOptionPane.showMessageDialog(null, "You must choose object to edit it");
        else {
            if(t1.isEditable() == true){
                try{
                    if(t1.getText().equals("") || t2.getText().equals("") || t3.getText().equals("") || t4.getText().equals("") || t5.getText().equals(""))
                        JOptionPane.showMessageDialog(null, "You must fill all fields");
                    else if(t1.getText().matches("\\W")||t1.getText().matches("\\d"))
                        JOptionPane.showMessageDialog(null, "Wrong Name");
                    else if(t2.getText().matches("\\W")||t2.getText().matches("\\d"))
                        JOptionPane.showMessageDialog(null, "Wrong Name");
                    else if(Long.parseLong(t3.getText())<1000000)
                        JOptionPane.showMessageDialog(null, "Wrong ID");
                    else if(Integer.parseInt(t4.getText())>100||Integer.parseInt(t4.getText())<18)
                        JOptionPane.showMessageDialog(null, "Wrong Age");
                    else if(Long.parseLong(t6.getText())>999999999||Integer.parseInt(t6.getText())<100000)
                        JOptionPane.showMessageDialog(null, "Wrong Phone Number");
                    else{                                                              
                        String firstName = t1.getText().trim();
                        String lastName = t2.getText().trim();
                        long ID = Long.parseLong(t3.getText());
                        int age = Integer.parseInt(t4.getText());
                        String address = t5.getText().trim();
                        long phoneNumber = Long.parseLong(t6.getText());
                        String gender = t7.getItemAt(t7.getSelectedIndex()).toString();
                        Person p = new Person(firstName, lastName, ID, age, address, phoneNumber, gender);                    
                        Person.persons.set(l.getSelectedIndex(), p);
                        t1.setEditable(false);
                        t2.setEditable(false);
                        t3.setEditable(false);
                        t4.setEditable(false);
                        t5.setEditable(false);                
                        t7.setEditable(false);
                        jButton2.setText("Edit");
                        jButton1.setEnabled(true);
                        jButton3.setEnabled(true);
                        jButton5.setEnabled(true);
                        l.setEnabled(true);
                        int temp = l.getSelectedIndex();
                        reFresh();
                        l.setSelectedIndex(temp);
                        try {
                            store();
                        } catch (FileNotFoundException ex) {
                            Logger.getLogger(ViewPersons.class.getName()).log(Level.SEVERE, null, ex);
                        } catch (IOException ex) {
                            Logger.getLogger(ViewPersons.class.getName()).log(Level.SEVERE, null, ex);
                        }
                        JOptionPane.showMessageDialog(null, "Changes saved!");
                    }
                    
                }
                catch(Exception e){
                    JOptionPane.showMessageDialog(null, "Invalid values");
                }
            } else if(t1.isEditable() == false){
                t1.setEditable(true);
                t2.setEditable(true);
                t3.setEditable(true);
                t4.setEditable(true);
                t5.setEditable(true);
                t7.setEditable(true);
                jButton1.setEnabled(false);
                jButton3.setEnabled(false);
                jButton5.setEnabled(false);
                l.setEnabled(false);
                jButton2.setText("Save");
            }
        }        
}//GEN-LAST:event_jButton2ActionPerformed

