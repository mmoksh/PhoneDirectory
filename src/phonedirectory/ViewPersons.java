    public void reFresh(){
        String [] names = new String[Person.persons.size()];
        for(int i = 0; i < names.length; i++){
            names [i] = (Person.persons.get(i)).getFirstName() + " " 
                    + (Person.persons.get(i)).getLastName();
        }
        l.setListData(names);        
        l.addListSelectionListener(new ListSelectionListener() {
                public void valueChanged(ListSelectionEvent e) {
                    if(l.isSelectionEmpty())
                        l.setSelectedIndex(0);
                    if(Person.persons.size()>0){
                        Person p = Person.persons.get(l.getSelectedIndex());
                        t1.setText(p.getFirstName());
                        t2.setText(p.getLastName());
                        t3.setText(String.valueOf(p.getID()));
                        t4.setText(String.valueOf(p.getAge()));
                        t5.setText(p.getAddress());
                        t6.setText(String.valueOf(p.getPhoneNumber()));
                        String gender = p.getGender();
                        if(gender.equals("Male"))
                            t7.setSelectedIndex(0);
                        else
                            t7.setSelectedIndex(1);
                    }
                    }
                });
    }
    
    public void store() throws FileNotFoundException, IOException{
        ObjectOutputStream output = new ObjectOutputStream(new FileOutputStream("Persons.dat"));
        for(int i = 0; i < Person.persons.size();i++){
            Person p = Person.persons.get(i);
            output.writeObject(p);
        }
        Person tail = null;
        output.writeObject(tail);        
        output.close();
    }
    
    public void load() throws IOException, ClassNotFoundException{
        try {
            ObjectInputStream input = new ObjectInputStream(new FileInputStream("Persons.dat"));
            Person temp = null;
            while((temp = (Person)input.readObject()) != null){
                Person.persons.add(temp);
            }
            input.close();
        } catch (FileNotFoundException ex) {
            ObjectOutputStream output = new ObjectOutputStream(new FileOutputStream("Persons.dat"));       
            Person tail = null;
            output.writeObject(tail);
            output.close();
        }
    }
    
    public void select(int i){
        l.setSelectedIndex(i);
        l.ensureIndexIsVisible(i);
    }
