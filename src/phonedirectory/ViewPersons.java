    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(t1.getText().equals(""))
            JOptionPane.showMessageDialog(null, "You must choose object to delete it");
        else {
            if(JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this object?", null, 0) == 0){
                int temp = l.getSelectedIndex();
                Person.persons.remove(l.getSelectedIndex());                
                reFresh();
                if(Person.persons.size()==temp)
                    l.setSelectedIndex(temp-1);
                else
                    l.setSelectedIndex(temp);
                if(Person.persons.size()>0){
                    try {
                        store();
                    } catch (FileNotFoundException ex) {
                        Logger.getLogger(ViewPersons.class.getName()).log(Level.SEVERE, null, ex);
                    } catch (IOException ex) {
                        Logger.getLogger(ViewPersons.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
                else{
                    ObjectOutputStream output;
                    try {
                        output = new ObjectOutputStream(new FileOutputStream("Persons.dat"));                        
                        Person tail = null;
                        output.writeObject(tail);
                        output.close();
                    } catch (IOException ex) {
                        Logger.getLogger(ViewPersons.class.getName()).log(Level.SEVERE, null, ex);
                    }                    
                    t1.setText("");
                    t2.setText("");
                    t3.setText("");
                    t4.setText("");
                    t5.setText("");
                    t6.setText("");
                    t7.setSelectedIndex(0);
                }
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

